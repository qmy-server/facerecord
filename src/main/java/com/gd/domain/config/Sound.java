package com.gd.domain.config;

public class Sound {
    private int soundID;
    private String soundEquipmentIp;
    private String username;
    private String password;

    public int getSoundID() {
        return soundID;
    }

    public void setSoundID(int soundID) {
        this.soundID = soundID;
    }

    public String getSoundEquipmentIp() {
        return soundEquipmentIp;
    }

    public void setSoundEquipmentIp(String soundEquipmentIp) {
        this.soundEquipmentIp = soundEquipmentIp;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
