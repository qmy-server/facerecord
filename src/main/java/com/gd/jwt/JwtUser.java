public class JwtUser extends Account implements UserDetails {

    private final Collection<? extends GrantedAuthority> authorities;


    public JwtUser(String id, String ifuse, String createTime, String updateTime, String orderNum, String username, String password, String salt, String token, String appId, String communicationId, Collection<? extends GrantedAuthority> authorities) {
        super(id, ifuse, createTime, updateTime, orderNum, username, password, salt, token, appId, communicationId);
        this.authorities = authorities;
    }


    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return authorities;
    }


    @JsonIgnore
    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @JsonIgnore
    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @JsonIgnore
    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @JsonIgnore
    @Override
    public boolean isEnabled() {
        return true;
    }

}
